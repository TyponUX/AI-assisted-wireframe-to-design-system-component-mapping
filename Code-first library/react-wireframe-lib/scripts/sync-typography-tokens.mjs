import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const root = process.cwd();
const roleMapPath = path.resolve(root, "../semantic-contract/catalog/typography-role-map.json");
const outDir = path.resolve(root, "src/generated");
const outPath = path.resolve(outDir, "typography-tokens.css");

const roleVarMap = {
  display: "display",
  heading_l: "heading-l",
  heading_m: "heading-m",
  heading_s: "heading-s",
  body_l: "body-l",
  body_m: "body-m",
  body_s: "body-s",
  label: "label",
  caption: "caption",
};

function buildRoleVars(roles, viewport) {
  const lines = [];
  for (const [role, values] of Object.entries(roles)) {
    const suffix = roleVarMap[role];
    if (!suffix) continue;
    const target = values[viewport];
    lines.push(`  --type-${suffix}-size: ${target.font_size_px}px;`);
    lines.push(`  --type-${suffix}-line: ${target.line_height_px}px;`);
    lines.push(`  --type-${suffix}-weight: ${target.font_weight};`);
    lines.push("");
  }

  return lines;
}

async function main() {
  const roleMapRaw = await readFile(roleMapPath, "utf8");
  const roleMap = JSON.parse(roleMapRaw);

  const desktopVars = buildRoleVars(roleMap.roles, "desktop");
  const mobileVars = buildRoleVars(roleMap.roles, "mobile");

  const css = [
    "/* Auto-generated by scripts/sync-typography-tokens.mjs. Do not edit manually. */",
    ":root {",
    `  --font-family-base: ${JSON.stringify(roleMap.base_font_family)};`,
    `  --font-weight-regular: ${roleMap.weights.regular};`,
    `  --font-weight-medium: ${roleMap.weights.medium};`,
    `  --font-weight-semibold: ${roleMap.weights.semibold};`,
    `  --font-weight-bold: ${roleMap.weights.bold};`,
    "",
    ...desktopVars,
    "}",
    "",
    `@media (max-width: ${roleMap.mobile_breakpoint_px}px) {`,
    "  :root {",
    ...mobileVars.map((line) => `  ${line}`),
    "  }",
    "}",
    "",
  ].join("\n");

  await mkdir(outDir, { recursive: true });
  await writeFile(outPath, css, "utf8");
  console.log(`Wrote ${path.relative(root, outPath)}`);
}

main().catch((error) => {
  console.error("Failed to sync typography tokens:", error);
  process.exit(1);
});
